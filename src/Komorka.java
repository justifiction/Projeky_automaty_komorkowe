public class Komorka {
    int rozmiar;
    int wartosc;
    int x;
    int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Komorka(int rozmiar, int wartosc){
        this.rozmiar=rozmiar;
        this.wartosc=wartosc;
    }
}
